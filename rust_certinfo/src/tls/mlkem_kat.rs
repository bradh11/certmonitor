// encapsulation key, because servers modulus-check the key before
// replying (an invalid one draws a handshake_failure alert instead of a
// ServerHello; see the live smoke test on issue #33).
//
// Provenance: NIST ACVP-Server ML-KEM-keyGen-FIPS203 known-answer test
// vectors, parameterSet "ML-KEM-768", tcId 26. Fetched from
// https://github.com/usnistgov/ACVP-Server (gen-val/json-files/
// ML-KEM-keyGen-FIPS203/internalProjection.json). Using a published KAT
// key keeps the constant traceable and reproducible; `mlkem_kat_tests`
// re-verifies its length and the FIPS 203 coefficient bound (every
// 12-bit coefficient < q = 3329) so a bad paste cannot slip through.
//
// This is a PUBLIC key with no secret counterpart in the tree; it
// carries no security value beyond "looks like a real client share".

